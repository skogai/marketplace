#!/usr/bin/env node
"use strict";

// Advisory-only scan for volatile lines sitting early in a memory file's
// prompt-cache prefix. Anthropic's prompt cache re-bills everything after
// the first changed byte, so a timestamp or generated-stamp near the top
// silently invalidates the cache on every session. This script never
// modifies anything — it only reports, same non-destructive guarantee as
// the skill that calls it.

const ZONE_FRACTION = 0.6;

const DATE_RE = /\b\d{4}-\d{2}-\d{2}\b/;
const TIME_RE = /\b\d{1,2}:\d{2}(?::\d{2})?\s?(?:AM|PM|am|pm)?\b/;
const STAMP_RE = /\b(AUTO-GENERATED|Last updated|Generated by)\b/i;
const UUID_RE = /\b[0-9a-f]{8}-[0-9a-f]{4}-4[0-9a-f]{3}/i;
const IMPORT_RE = /^\s*@import\b/;
const IMPORT_STAMP_RE = /\b\d{4}-\d{2}-\d{2}\b|build[-_]?\d+|generated/i;

// Order matters: a line is reported once per category it matches, in this
// priority order, and a UUID substring never also gets counted as a time.
const CATEGORIES = [
  { name: "import", test: (line) => IMPORT_RE.test(line) && IMPORT_STAMP_RE.test(line) },
  { name: "uuid", test: (line) => UUID_RE.test(line) },
  { name: "stamp", test: (line) => STAMP_RE.test(line) },
  { name: "date", test: (line) => DATE_RE.test(line) },
  { name: "time", test: (line) => TIME_RE.test(line) },
];

function zoneEnd(totalLines) {
  return Math.ceil(totalLines * ZONE_FRACTION);
}

function scan(text) {
  const lines = text.split(/\r?\n/);
  const end = zoneEnd(lines.length);
  const findings = [];
  for (let i = 0; i < end; i++) {
    const line = lines[i];
    for (const category of CATEGORIES) {
      if (category.test(line)) {
        findings.push({ line: i + 1, category: category.name, text: line.trim() });
      }
    }
  }
  return findings;
}

function formatReport(findings) {
  if (findings.length === 0) {
    return "cache-stability: no volatile lines found in the first 60% of the file.";
  }
  const lines = findings.map((f) => `L${f.line} [${f.category}]: ${f.text}`);
  lines.push(
    "",
    `cache-stability: ${findings.length} volatile line(s) found in the first 60% of the file — ` +
      "these lines change across sessions and sit early in the prompt-cache prefix; every change " +
      "re-bills the whole prefix — move them to the end of the file or remove them."
  );
  return lines.join("\n");
}

function main() {
  const [filePath] = process.argv.slice(2);
  if (!filePath) {
    console.error("Usage: cache-stability-check.js <file>");
    process.exit(1);
  }
  const fs = require("fs");
  let text;
  try {
    text = fs.readFileSync(filePath, "utf-8");
  } catch (err) {
    console.error(`cache-stability-check: cannot read ${filePath}: ${err.message}`);
    process.exit(1);
  }
  const findings = scan(text);
  console.log(formatReport(findings));
  process.exit(0);
}

if (require.main === module) main();

module.exports = { scan, formatReport, zoneEnd };
